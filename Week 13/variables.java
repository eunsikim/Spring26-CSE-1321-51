public class variables {
    public static void main(String[] args) {
        byte age = 20;
        String message = "Hello World"; // Double-quote for Strings
        char initial = 'E'; // Single-quote for chars

        // When you type a number with decimal point,
        // Java will assume it is a double data type, not float.
        // We add an 'f' at the end to tell the computer this is a float data type
        float temperature = 70.5f; 
    }
}

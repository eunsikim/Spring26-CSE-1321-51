import math

def main():
    # Constant mathematical values
    print(math.pi)
    print(math.e)
    print(math.inf)
    print(math.nan)

    # Round up to nearest integer
    print(math.ceil(math.pi))
    # Round down to nearest integer
    print(math.floor(math.pi))
    # Absolute value
    print(math.fabs(-50))
    # Square Root
    print(math.sqrt(144))
    # Cube Root
    print(math.cbrt(64))
    # Logarithm
    print(math.log(16, 2))

if __name__ == "__main__":
    main()